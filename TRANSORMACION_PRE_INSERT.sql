---------------------------------------------------------- FORMATO DE TIEMPO
SELECT DISTINCT ID_TIEMPO, 
    REGEXP_SUBSTR(ID_TIEMPO, '[^/]+',1,1) AS DIA,
    REGEXP_SUBSTR(ID_TIEMPO, '[^/]+',1,2) AS MES,
    REGEXP_SUBSTR(ID_TIEMPO, '[^/]+',1,3) AS ANIO,
CASE REGEXP_SUBSTR(ID_TIEMPO, '[^/]+',1,2)
    WHEN '01' THEN 1
    WHEN '02' THEN 1
    WHEN '03' THEN 2
    WHEN '04' THEN 2
    WHEN '05' THEN 3
    WHEN '06' THEN 3
    WHEN '07' THEN 4
    WHEN '08' THEN 4
    WHEN '09' THEN 5
    WHEN '10' THEN 5
    WHEN '11' THEN 6
    WHEN '12' THEN 6
    ELSE NULL 
END AS BIMESTRE
FROM ORDENES ORDER BY ID_TIEMPO;

