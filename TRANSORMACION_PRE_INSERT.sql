---------------------------------------------------------- FORMATO DE ORDEN
SELECT O.*, P.RESTANTES, DECODE(P.RESTANTES,0, 1, 0) AS ORDEN_CUMPLIDA
FROM 
    ORDENES O, 
    (SELECT ID_ORDEN, SUM(UNIDADES_ORDENADAS) - SUM(UNIDADES_FACTURADAS) AS RESTANTES FROM ORDENES GROUP BY ID_ORDEN) P
WHERE O.ID_ORDEN = P.ID_ORDEN;

