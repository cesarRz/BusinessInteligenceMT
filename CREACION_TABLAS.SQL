CREATE TABLE EST_TIEMPO(
	ID_TIEMPO DATE PRIMARY KEY,
	ANIO INTEGER,
	BIMESTRE INTEGER,
	MES INTEGER,
	DIA INTEGER
);

CREATE TABLE EST_ESTADO (
	id_estado VARCHAR2(20) primary key, 
	nombre varchar2(255), 
	region varchar(255), 
	numero_region INTEGER,
	NOMBRE_ORIGINAL varchar2(255)
);

CREATE TABLE EST_PRODUCTO (
	ID_PRODUCTO INTEGER PRIMARY KEY,
	MARCA VARCHAR2(255),
	VARIANTE VARCHAR2(255),
	CATEGORIA VARCHAR2(255),
	PRESENTACION VARCHAR2(255),
	PROMOCION INTEGER,
	UNIDAD VARCHAR2(255),
	ORIGINAL VARCHAR(255)
);

CREATE TABLE EST_CLIENTE (
	id_cliente INTEGER primary key, 
	nombre varchar2(255)
);


CREATE TABLE EST_ORDENES (	
	ID_ORDEN NUMBER(38,0), 
	ID_TIEMPO DATE, 
	ID_CLIENTE NUMBER(38,0), 
	ID_ESTADO VARCHAR2(26 BYTE), 
	ID_PRODUCTO NUMBER(38,0), 
	UNIDADES_ORDENADAS NUMBER(38,0), 
	UNIDADES_FACTURADAS NUMBER(38,0), 
	IMPORTE_ORDENADO NUMBER(38,2), 
	IMPORTE_FACTURADO NUMBER(38,2),
	ORDEN_CUMPLIDA INT
);


